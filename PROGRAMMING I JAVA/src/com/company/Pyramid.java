package com.company;

public class Pyramid {
    public static void main(String[] args) {

        for (int k = 0; k <= 5 - 0; k++) {
            System.out.print(" ");
        }
        for (int j = 0; j <= 0; j++) {
            System.out.print(" *");
        }
        System.out.println();

        for (int k = 0; k <= 5 - 1; k++) {
            System.out.print(" ");
        }
        for (int j = 0; j <= 1; j++) {
            System.out.print(" *");
        }
        System.out.println();

        for (int k = 0; k <= 5 - 2; k++) {
            System.out.print(" ");
        }
        for (int j = 0; j <= 2; j++) {
            System.out.print(" *");
        }
        System.out.println();

        for (int k = 0; k <= 5 - 3; k++) {
            System.out.print(" ");
        }
        for (int j = 0; j <= 3; j++) {
            System.out.print(" *");
        }
        System.out.println();

        for (int k = 0; k <= 5 - 4; k++) {
            System.out.print(" ");
        }
        for (int j = 0; j <= 4; j++) {
            System.out.print(" *");
        }
        System.out.println();
    }
}
